# ── 关键词索引（启动时构建）──
_KEYWORD_INDEX = None


def _build_keyword_index():
    """构建关键词到FAQ条目的索引"""
    global _KEYWORD_INDEX
    if _KEYWORD_INDEX is not None:
        return _KEYWORD_INDEX
    
    _KEYWORD_INDEX = {}
    for i, faq in enumerate(FAQ_DATABASE):
        for keyword in faq.get("keywords", []):
            keyword_lower = keyword.lower()
            if keyword_lower not in _KEYWORD_INDEX:
                _KEYWORD_INDEX[keyword_lower] = []
            _KEYWORD_INDEX[keyword_lower].append(i)
    
    return _KEYWORD_INDEX


def search_faq(query: str, top_k: int = 3) -> list[dict]:
    """搜索FAQ知识库，返回匹配的条目
    
    Args:
        query: 用户问题
        top_k: 返回最多几个结果
        
    Returns:
        匹配的FAQ条目列表，按匹配度排序
    """
    if not query or not query.strip():
        return []
    
    index = _build_keyword_index()
    query_lower = query.lower()
    
    # 计算每个FAQ条目的匹配分数
    scores = {}
    for keyword, faq_indices in index.items():
        if keyword in query_lower:
            for idx in faq_indices:
                scores[idx] = scores.get(idx, 0) + 1
    
    # 按分数排序
    sorted_indices = sorted(scores.keys(), key=lambda x: scores[x], reverse=True)
    
    # 返回 top_k 结果
    results = []
    for idx in sorted_indices[:top_k]:
        faq = FAQ_DATABASE[idx].copy()
        faq["match_score"] = scores[idx]
        results.append(faq)
    
    return results


def format_faq_context(faqs: list[dict]) -> str:
    """将FAQ条目格式化为上下文文本，注入Prompt"""
    if not faqs:
        return ""
    
    parts = ["FAQ知识库参考："]
    for i, faq in enumerate(faqs, 1):
        parts.append(f"\n{i}. 问题：{faq['question']}")
        parts.append(f"   答案：{faq['answer']}")
    
    return "\n".join(parts)
